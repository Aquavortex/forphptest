<script type="text/javascript">
$(document).ready(function(){
    //Open modal
    $('.openModal').click(function(e) {
        e.preventDefault();
        $(".modal").addClass('show');
        $(".modal-dialog").addClass('animate');
        
    });
    //Close modal
    $(".modal, .modal__close").click(function(e) {
        if (e.target == this) { 
            e.preventDefault();
            $(".modal").removeClass('show');
            $(".modal-dialog").removeClass('animate');
        }
    })
});
